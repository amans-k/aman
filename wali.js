function createCalendarRows() {
    const calendarBody = document.getElementById('calendarBody');
    
    ramzanData.forEach(day => {
        const row = document.createElement('div');
        row.className = `calendar-row ${day.specialDay ? 'special-day' : ''}`;
        
        row.innerHTML = `
            <div>${day.date}</div>
            <div>${day.islamicDate}</div>
            <div>${day.sehriEnds}</div>
            <div>${day.iftarTime}</div>
            <div>${day.specialDay}</div>
        `;
        
        calendarBody.appendChild(row);
    });
}

// Initialize the calendar when the page loads
document.addEventListener('DOMContentLoaded', createCalendarRows); 